//Count byte of every Datatype

#include <iostream>
using namespace std;
int main()
{
    cout << "Size of the integer is : " << sizeof(int) << "\n";
    cout << "Size of the float is : " << sizeof(float) << "\n";
    cout << "Size of the double is : " << sizeof(double) << "\n";
    cout << "Size of the character is : " << sizeof(char) << "\n";
    cout << "Size of the boolean is : " << sizeof(bool) << "\n";
    return 0;
}